def tashizan(a, b):
    '''
    説明: 足し算をする関数
    入力: 数字a, 数字b
    出力: 計算結果(a + b)
    '''
    # 計算結果をkekkaという変数に入れる
    kekka = a + b
    # 計算結果が入ったkekkaを出力する
    return kekka

